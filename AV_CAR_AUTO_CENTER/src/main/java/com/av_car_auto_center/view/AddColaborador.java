    private void BntAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntAddActionPerformed
        String nome = TxFNome.getText().trim();

        if (ListFuncao.getSelectedIndices().length == 0) {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos uma função.");
            return;
        }

        Colaborador colaborador = new Colaborador();
        colaborador.setNome(nome);
        colaborador.setAtivo(true);

        int idGerado = colaboradorController.cadastrarComId(colaborador);

        if (idGerado == -1) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar colaborador.");
            return;
        }

        colaborador.setID(idGerado);

        for (int i : ListFuncao.getSelectedIndices()) {
            Funcao funcao = funcoesList.get(i);
            ColaboradorFuncao cf = new ColaboradorFuncao(colaborador, funcao);
            colaboradorController.vincularFuncao(cf);
        }

        TxFNome.setText("");
        ListFuncao.clearSelection();
    }//GEN-LAST:event_BntAddActionPerformed


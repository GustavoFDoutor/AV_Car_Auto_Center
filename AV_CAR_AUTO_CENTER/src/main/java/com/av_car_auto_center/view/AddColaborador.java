    private List<Funcao> funcoesList;
    private ColaboradorController colaboradorController = new ColaboradorController();

    private void carregarFuncoes() {
        FuncaoController controller = new FuncaoController();
        funcoesList = controller.listarAtivos();

        DefaultListModel<String> listModel = new DefaultListModel<>();
        for (Funcao f : funcoesList) {
            listModel.addElement(f.getDescricao());
        }

        ListFuncao.setModel(listModel);
        ListFuncao.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
    }


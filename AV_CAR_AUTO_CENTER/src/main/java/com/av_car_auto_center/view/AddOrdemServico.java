    private void calcularValor() {
        double total = 0;

        for (int i : ListServicos.getSelectedIndices()) {
            total += servicosList.get(i).getValor();
        }
        for (int i : ListPecas.getSelectedIndices()) {
            total += pecasList.get(i).getValor();
        }

        LabelValor.setText(String.format("%.2f", total));
    }
    
    private void carregarServicos() {
        servicosList = servicoController.listarAtivos();
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Servico s : servicosList) {
            model.addElement(s.getDescricao() + " - R$ " + String.format("%.2f", s.getValor()));
        }
        ListServicos.setModel(model);
        ListServicos.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
    }

    private void carregarPecas() {
        pecasList = pecaController.listarAtivos();
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Peca p : pecasList) {
            model.addElement(p.getDescricao() + " - R$ " + String.format("%.2f", p.getValor()));
        }
        ListPecas.setModel(model);
        ListPecas.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
    }


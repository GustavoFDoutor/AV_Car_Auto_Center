    private void TxFCPFFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TxFCPFFocusLost
        String cpf = TxFCPF.getText().trim();
        if (cpf.isEmpty()) return;

        Cliente cliente = clienteController.buscarPorCPF(cpf);
        if (cliente == null) {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado.");
            CbBVeiculo.removeAllItems();
            veiculosList.clear();
            return;
        }

        List<ClienteVeiculo> clienteVeiculos = clienteController.listarVeiculosDoCliente(cliente.getID());
        CbBVeiculo.removeAllItems();
        veiculosList.clear();

        if (clienteVeiculos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cliente não possui veículos vinculados.");
            return;
        }

        for (ClienteVeiculo cv : clienteVeiculos) {
            Veiculo veiculo = veiculoController.buscarPorID(cv.getVeiculo().getID());
            if (veiculo != null) {
                veiculosList.add(veiculo);
                CbBVeiculo.addItem(veiculo.getPlaca());
            }
        }
    }//GEN-LAST:event_TxFCPFFocusLost

    private void BntAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntAddActionPerformed
        String cpf = TxFCPF.getText().trim();
        String dataStr = TxFData.getText().trim();

        if (CbBVeiculo.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um veículo.");
            return;
        }

        if (ListServicos.getSelectedIndices().length == 0 && ListPecas.getSelectedIndices().length == 0) {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos um serviço ou peça.");
            return;
        }

        Cliente cliente = clienteController.buscarPorCPF(cpf);
        if (cliente == null) {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado.");
            return;
        }

        Date dataAbertura;
        try {
            dataAbertura = new java.text.SimpleDateFormat("dd/MM/yyyy").parse(dataStr);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data inválida. Use o formato dd/MM/yyyy.");
            return;
        }

        double valor = 0;
        for (int i : ListServicos.getSelectedIndices()) {
            valor += servicosList.get(i).getValor();
        }
        for (int i : ListPecas.getSelectedIndices()) {
            valor += pecasList.get(i).getValor();
        }

        Veiculo veiculoSelecionado = veiculosList.get(CbBVeiculo.getSelectedIndex());

        OrdemServico ordemServico = new OrdemServico(
            0,
            veiculoSelecionado,
            cliente.getNomeRazaoSocial(),
            dataAbertura,
            null,
            valor,
            "Aguardando Pagamento"
        );

        int idGerado = ordemServicoController.cadastrarComId(ordemServico);

        if (idGerado == -1) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar ordem de serviço.");
            return;
        }

        ordemServico.setID(idGerado);

        // Vincula os serviços selecionados
        for (int i : ListServicos.getSelectedIndices()) {
            OrdemServicoServico oss = new OrdemServicoServico(ordemServico, servicosList.get(i));
            ordemServicoController.vincularServico(oss);
        }

        // Vincula as peças selecionadas
        for (int i : ListPecas.getSelectedIndices()) {
            OrdemServicoPeca osp = new OrdemServicoPeca(ordemServico, pecasList.get(i));
            ordemServicoController.vincularPeca(osp);
        }
        
        PagarOrdemServico telaPagamento = new PagarOrdemServico(ordemServico);
        telaPagamento.setVisible(true);

        // Limpa os campos
        TxFCPF.setText("");
        TxFData.setText("");
        LabelValor.setText("");
        CbBVeiculo.removeAllItems();
        veiculosList.clear();
        ListServicos.clearSelection();
        ListPecas.clearSelection();
    }//GEN-LAST:event_BntAddActionPerformed


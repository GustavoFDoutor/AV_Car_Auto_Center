    private void BntAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntAddActionPerformed
        String codigo = TxFCodigo.getText().trim();
        String descricao = TxFDesc.getText().trim();
        String valorStr = TxFValor.getText().trim();

        int prazo = (int) SpnPrazo.getValue();

        if (CbBFornecedor.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um fornecedor.");
            return;
        }

        double valor;
        try {
            valor = Double.parseDouble(valorStr.replace(",", "."));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Valor inválido.");
            return;
        }

        Fornecedor fornecedor = fornecedoresList.get(
                CbBFornecedor.getSelectedIndex()
        );

        Peca peca = new Peca(
                0,
                true,
                fornecedor,
                codigo,
                descricao,
                prazo,
                valor
        );

        pecaController.cadastrar(peca);

        TxFCodigo.setText("");
        TxFDesc.setText("");
        TxFValor.setText("");
        SpnPrazo.setValue(0);
        CbBFornecedor.setSelectedIndex(-1);
    }//GEN-LAST:event_BntAddActionPerformed


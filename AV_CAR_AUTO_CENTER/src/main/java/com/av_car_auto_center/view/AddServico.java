    private void BntAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntAddActionPerformed
        String descricao = TxFDesc.getText().trim();
        int prazo = (int) SpnPrazo.getValue();
        String valorStr = TxFValor.getText().trim();

        String tipoServico = "";
        if (RBntInterno.isSelected()) {
            tipoServico = "Interno";
        } else if (RBntTerceirizado.isSelected()) {
            tipoServico = "Terceirizado";
        } else {
            JOptionPane.showMessageDialog(null, "Selecione o Tipo de Serviço.");
            return;
        }

        double valor;
        try {
            valor = Double.parseDouble(valorStr.replace(",", "."));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Valor inválido.");
            return;
        }

        Servico servico = new Servico();
        servico.setDescricao(descricao);
        servico.setPrazoGarantia(prazo);
        servico.setTipoServico(tipoServico);
        servico.setValor(valor);
        servico.setAtivo(true);

        servicoController.cadastrar(servico);

        TxFDesc.setText("");
        SpnPrazo.setValue(0);
        RBntInterno.setSelected(false);
        RBntTerceirizado.setSelected(false);
        TxFValor.setText("");
    }//GEN-LAST:event_BntAddActionPerformed


    private void BntBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntBuscarActionPerformed
        try {

            String entrada = JOptionPane.showInputDialog(null,"Digite o ID do Cliente:");

            // Usuário clicou em Cancelar
            if (entrada == null) {
                return;
            }

            // Campo vazio
            if (entrada.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null,"Digite um ID!");
                return;
            }

            int id = Integer.parseInt(entrada);

            ClienteController controller = new ClienteController();

            Cliente cliente = controller.buscarPorID(id);

            if (cliente == null) {

                JOptionPane.showMessageDialog(null,"Cliente não encontrado!");

                return;
            }

            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("ID");
            modelo.addColumn("Tipo");
            modelo.addColumn("Nome / Razão Social");
            modelo.addColumn("CPF");
            modelo.addColumn("Telefone");
            modelo.addColumn("Email");
            modelo.addColumn("ID Veículo");

            List<ClienteVeiculo> veiculos =
                    controller.listarVeiculosDoCliente(
                            cliente.getID()
                    );

            String idsVeiculos = "";

            for (ClienteVeiculo cv : veiculos) {

                idsVeiculos += cv.getVeiculo().getID() + " ";
            }

            modelo.addRow(new Object[]{
                cliente.getID(),
                cliente.getTipoCliente(),
                cliente.getNomeRazaoSocial(),
                cliente.getCPF(),
                cliente.getTelefone(),
                cliente.getEmail(),
                idsVeiculos.trim()
            });

            TableClientes.setModel(modelo);

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, "O ID deve ser um número!");

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null,"Erro ao buscar cliente: " + e.getMessage());
        }
    }//GEN-LAST:event_BntBuscarActionPerformed


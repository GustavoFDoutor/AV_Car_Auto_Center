    private void carregarTabela(List<Fornecedor> fornecedores) {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("IDFornecedor");
        modelo.addColumn("Ativo");
        modelo.addColumn("NomeFornecedor");
        modelo.addColumn("Telefone");
        modelo.addColumn("Email");

        for (Fornecedor f : fornecedores) {

            modelo.addRow(new Object[]{
                f.getID(),
                f.isAtivo() ? "Sim" : "Não",
                f.getNomeFornecedor(),
                f.getTelefone() != null
                        ? f.getTelefone()
                        : "Não informado",
                f.getEmail() != null
                        ? f.getEmail()
                        : "Não informado"
            });
        }

        TableFornecedor.setModel(modelo);
    }


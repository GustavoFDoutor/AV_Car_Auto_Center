    private void carregarTabela(List<Funcao> funcoes) {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("IDFunção");
        modelo.addColumn("Ativo");
        modelo.addColumn("Descrição");

        for (Funcao f : funcoes) {

            modelo.addRow(new Object[]{
                f.getID(),
                f.isAtivo() ? "Sim" : "Não",
                f.getDescricao()
            });
        }

        TableFuncao.setModel(modelo);
    }


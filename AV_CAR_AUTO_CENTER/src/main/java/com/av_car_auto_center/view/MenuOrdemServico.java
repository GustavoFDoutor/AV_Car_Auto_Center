    private void BntBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntBuscarActionPerformed
        try {

            String entrada = JOptionPane.showInputDialog(
                    null,
                    "Digite o ID da Ordem de Serviço:"
            );

            if (entrada == null) {
                return;
            }

            if (entrada.trim().isEmpty()) {

                JOptionPane.showMessageDialog(
                        null,
                        "Digite um ID!"
                );

                return;
            }

            int id = Integer.parseInt(entrada);

            OrdemServicoController controller =
                    new OrdemServicoController();

            OrdemServico ordem =
                    controller.buscarPorID(id);

            if (ordem == null) {

                JOptionPane.showMessageDialog(
                        null,
                        "Ordem de Serviço não encontrada!"
                );

                return;
            }

            List<OrdemServico> lista =
                    new ArrayList<>();

            lista.add(ordem);

            carregarTabela(lista);

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Digite um número válido!"
            );

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Erro ao buscar Ordem de Serviço: "
                    + e.getMessage()
            );
        }
    }//GEN-LAST:event_BntBuscarActionPerformed

    private void BntStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntStatusActionPerformed
        try {
            int linhaSelecionada = TableOS.getSelectedRow();

            if (linhaSelecionada == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma Ordem de Serviço!");
                return;
            }

            int idOS = Integer.parseInt(
                    TableOS.getValueAt(linhaSelecionada, 0).toString()
            );

            OrdemServicoController controller = new OrdemServicoController();
            OrdemServico ordem = controller.buscarPorID(idOS);

            if (ordem == null) {
                JOptionPane.showMessageDialog(null, "Ordem de Serviço não encontrada!");
                return;
            }

            if (ordem.getStatus().equals("Aguardando Pagamento")) {
                JOptionPane.showMessageDialog(null, "Essa ordem precisa ser paga antes de entrar em execução.");
                return;
            }

            if (ordem.getStatus().equals("Finalizada")) {
                JOptionPane.showMessageDialog(null, "Essa ordem já está finalizada.");
                return;
            }

            int confirmacao = JOptionPane.showConfirmDialog(
                    null,
                    "Deseja finalizar esta Ordem de Serviço?",
                    "Finalizar OS",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirmacao != JOptionPane.YES_OPTION) {
                return;
            }

            java.util.Date dataHoje = new java.util.Date();

            if (dataHoje.before(ordem.getDataAbertura())) {
                JOptionPane.showMessageDialog(
                        null,
                        "Não é possível finalizar a OS antes da Data de Abertura."
                );
                return;
            }

            ordem.setStatus("Finalizada");
            ordem.setDataFinalizacao(dataHoje);

            controller.atualizar(ordem);

            JOptionPane.showMessageDialog(
                    null,
                    "Ordem de Serviço finalizada com sucesso!"
            );

            carregarTabela(controller.listarTodos());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null,
                    "Erro ao alterar status: " + e.getMessage()
            );
        }
    }//GEN-LAST:event_BntStatusActionPerformed

    private void BntPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntPagarActionPerformed
        try {
            int linhaSelecionada = TableOS.getSelectedRow();

            if (linhaSelecionada == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma Ordem de Serviço!");
                return;
            }

            int idOS = Integer.parseInt(
                    TableOS.getValueAt(linhaSelecionada, 0).toString()
            );

            OrdemServicoController controller = new OrdemServicoController();
            OrdemServico ordem = controller.buscarPorID(idOS);

            if (ordem == null) {
                JOptionPane.showMessageDialog(null, "Ordem de Serviço não encontrada!");
                return;
            }

            if (!ordem.getStatus().equals("Aguardando Pagamento")) {
                JOptionPane.showMessageDialog(null, "Essa ordem não está aguardando pagamento.");
                return;
            }

            PagarOrdemServico telaPagamento = new PagarOrdemServico(ordem);
            telaPagamento.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao abrir pagamento: " + e.getMessage());
        }
    }//GEN-LAST:event_BntPagarActionPerformed
    
    private void carregarTabela(List<OrdemServico> ordens) {

        DefaultTableModel modelo = new DefaultTableModel();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        modelo.addColumn("IDOrdemServico");
        modelo.addColumn("NomeCliente");
        modelo.addColumn("DataAbertura");
        modelo.addColumn("DataFinalização");
        modelo.addColumn("Status");
        modelo.addColumn("Valor");
        modelo.addColumn("Placa");

        for (OrdemServico o : ordens) {
            modelo.addRow(new Object[]{
                o.getID(),
                o.getNomeCliente(),
                formato.format(o.getDataAbertura()),
                o.getDataFinalizacao() != null ? formato.format(o.getDataFinalizacao()) : "Em aberto",
                o.getStatus(),
                String.format("R$ %.2f", o.getValor()),
                o.getVeiculo().getPlaca()
            });
        }

        TableOS.setModel(modelo);
    }
    

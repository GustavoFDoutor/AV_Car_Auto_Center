    private void carregarTabela(List<Pagamento> pagamentos) {

        DefaultTableModel modelo = new DefaultTableModel();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        modelo.addColumn("IDPagamento");
        modelo.addColumn("Data do Pagamento");
        modelo.addColumn("Valor");

        for (Pagamento p : pagamentos) {
            modelo.addRow(new Object[]{
                p.getID(),
                formato.format(p.getDataPagamento()),
                String.format("R$ %.2f", p.getValor())
            });
        }

        TablePagamentos.setModel(modelo);
    }


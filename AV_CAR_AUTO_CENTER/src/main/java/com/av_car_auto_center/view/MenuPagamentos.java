    private void BntBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntBuscarActionPerformed
        try {
            String entrada = JOptionPane.showInputDialog(
                    null,
                    "Digite o ID do Pagamento:"
            );

            if (entrada == null) {
                return;
            }

            if (entrada.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite um ID!");
                return;
            }

            int id = Integer.parseInt(entrada);

            PagamentoController controller = new PagamentoController();

            Pagamento pagamento = controller.buscarPorID(id);

            if (pagamento == null) {
                JOptionPane.showMessageDialog(null, "Pagamento não encontrado!");
                return;
            }

            List<Pagamento> lista = new ArrayList<>();
            lista.add(pagamento);

            carregarTabela(lista);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um número válido!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    null,
                    "Erro ao buscar pagamento: " + e.getMessage()
            );
        }
    }//GEN-LAST:event_BntBuscarActionPerformed


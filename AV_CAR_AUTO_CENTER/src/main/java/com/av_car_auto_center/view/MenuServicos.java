    private void carregarTabela(List<Servico> servicos) {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("IDServico");
        modelo.addColumn("Ativo");
        modelo.addColumn("Descrição");
        modelo.addColumn("Prazo Garantia");
        modelo.addColumn("Tipo Serviço");
        modelo.addColumn("Valor");

        for (Servico s : servicos) {

            modelo.addRow(new Object[]{
                s.getID(),
                s.isAtivo() ? "Sim" : "Não",
                s.getDescricao(),
                s.getPrazoGarantia(),
                s.getTipoServico(),
                String.format("R$ %.2f", s.getValor())
            });
        }

        TableServico.setModel(modelo);
    }


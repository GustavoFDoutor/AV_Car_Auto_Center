    private void carregarTabela(List<Veiculo> veiculos) {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("IDVeiculo");
        modelo.addColumn("Ativo");
        modelo.addColumn("Placa");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("AnoFab");
        modelo.addColumn("AnoModelo");
        modelo.addColumn("CPF Cliente");

        for (Veiculo v : veiculos) {

            modelo.addRow(new Object[]{
                v.getID(),
                v.isAtivo() ? "Sim" : "Não",
                v.getPlaca(),
                v.getMarca(),
                v.getModelo(),
                v.getAnoFabricacao(),
                v.getAnoModelo(),
                v.getCPFCliente() != null
                        ? v.getCPFCliente()
                        : "Sem cliente"
            });
        }

        TableVeiculo.setModel(modelo);
    }
    

